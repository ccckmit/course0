#include <stdio.h>

/*
 * ==========================================
 * 這是一個位於全域區塊的多行註解。
 * 它可以跨越多行。
 * 用來測試 C4 的詞法分析器是否能正確跳過這些字元，
 * 並且精準地遞增行號 (line variable)。
 * ==========================================
 */

int main() {
    // 變數宣告必須在函式開頭
    int x = 10;
    int y = 20;
    int z = 0;

    /* 這是一個單行的區塊註解 */
    z = x + y;

    /* 
       這段多行註解包住了正常的程式碼，
       如果 C4 發生 Bug，這些程式碼就會被執行！
       
       z = 999;
       printf("!!!!!!!! 這行絕對不應該被印出來 !!!!!!!!\n");
    */
    
    printf("x = %d, y = %d\n", x, y);
    printf("z = x + y = %d\n", z);

    /* 
     * 【測試精準錯誤提示】
     * 如果你把下面那一行的雙斜線 // 刪掉，
     * C4 應該要印出非常精準的錯誤行號與 ^ 指標！
     */
    // z = z + ; 
    
    printf("Multi-line comments work perfectly!\n");
    return 0;
}