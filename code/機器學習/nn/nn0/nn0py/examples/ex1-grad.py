"""
ex1-grad.py — 基礎自動微分運算演示

本程式展示自動微分引擎的基本功能：
  1. 建立帶有初始值的參數與變數
  2. 建立運算圖（前向傳播）
  3. 反向傳播計算梯度

執行方式：
  python ex1-grad.py
"""

from nn0 import Value


# ===================== 1. 建立具有初始值的參數與變數 =====================

# 建立三個 Value 節點，代表計算圖中的葉節點
# 這些節點將會被用來建立較複雜的運算
#
# a = 3.0：將作為係數 a
# b = 2.0：將作為常數項 b
# x = 4.0：將作為變數 x
a = Value(3.0)
b = Value(2.0)
x = Value(4.0)


# ===================== 2. 建立運算圖 (Forward pass) =====================

# 建立複雜的運算：y = a * x^2 + b
#
# 這個運算會自動建立計算圖：
#   1. x ** 2：計算 x 的平方
#   2. a * (x ** 2)：計算 a 乘以 x 的平方
#   3. + b：加上常數項 b
#
# 數學意義：
#   y = a * x^2 + b = 3 * 4^2 + 2 = 3 * 16 + 2 = 50
y = a * (x ** 2) + b


# ===================== 3. 顯示前向傳播結果 =====================

# 打印標題
print("=== 1. 基礎自動微分運算 ===")

# 打印前向計算結果
# y.data 是 Value 節點的數值屬性
print(f"前向計算結果 y.data = {y.data:.4f}")  # 預期：3 * 16 + 2 = 50


# ===================== 4. 反向傳播計算梯度 (Backward pass) =====================

# 呼叫 backward() 方法開始反向傳播
#
# 反向傳播的原理（鏈式法則）：
#   假設 y = f(g(x))，則 dy/dx = df/dg * dg/dx
#
# 在我們的例子中：
#   y = a * x^2 + b
#   dy/da = x^2 = 16
#   dy/db = 1
#   dy/dx = 2ax = 2*3*4 = 24
#
# backward() 會沿著計算圖反向傳遞梯度
y.backward()


# ===================== 5. 顯示梯度並與理論值比對 =====================

# 打印計算出的梯度
# a.grad 是 loss 對 a 的偏導數 dy/da
print(f"dy/da = {a.grad:.4f} (理論值 x^2 = 16)")

# b.grad 是 loss 對 b 的偏導數 dy/db
print(f"dy/db = {b.grad:.4f} (理論值 1)")

# x.grad 是 loss 對 x 的偏導數 dy/dx
print(f"dy/dx = {x.grad:.4f} (理論值 2ax = 2*3*4 = 24)")

# 驗證：所有梯度都應該與理論值匹配！