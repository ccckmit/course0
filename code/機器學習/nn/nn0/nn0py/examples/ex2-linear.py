"""
ex2-linear.py — 線性回歸訓練演示

本程式展示如何使用自動微分引擎和 Adam 優化器來訓練一個線性回歸模型：

執行方式：
  python ex2-linear.py
"""

from nn0 import Value, Adam


# ===================== 1. 準備訓練數據 =====================

# 準備訓練數據 (目標函數 y = 2x + 1)
#
# 我們使用四個數據點來擬合一條直線
# x 是輸入，y 是目標輸出
X = [1.0, 2.0, 3.0, 4.0]
Y = [3.0, 5.0, 7.0, 9.0]

# 打印標題
print("\n=== 2. 線性回歸訓練 ===")


# ===================== 2. 初始化模型參數 =====================

# 初始化模型參數 (權重與偏差)
#
# w：權重（斜率），初始化為 0
# b：偏差（截距），初始化為 0
#
# 目標：學習到 w ≈ 2, b ≈ 1
w = Value(0.0)
b = Value(0.0)


# ===================== 3. 實例化 Adam 優化器 =====================

# 建立 Adam 優化器
# 參數：
#   [w, b]：要優化的參數列表
#   lr=0.1：學習率，較大因為模型簡單
optimizer = Adam([w, b], lr=0.1)


# ===================== 4. 訓練迴圈 =====================

# 進行 50 次訓練迭代
for epoch in range(51):
    # -------------------- 前向傳播 --------------------
    # 預測值：y_pred = w * x + b
    preds = [w * x + b for x in X]

    # 計算 Mean Squared Error (MSE) Loss
    # Loss = (1/n) * Σ(y_pred - y)^2
    losses = [(pred - y) ** 2 for pred, y in zip(preds, Y)]
    loss = sum(losses) / len(losses)

    # -------------------- 反向傳播與參數更新 --------------------
    # 反向傳播：計算梯度
    loss.backward()

    # Adam 更新：更新參數並清除梯度
    optimizer.step()

    # 每 10 個 epoch 打印一次進度
    if epoch % 10 == 0:
        print(f"Epoch {epoch:2d} | Loss: {loss.data:.4f} | w: {w.data:.4f}, b: {b.data:.4f}")


# ===================== 學習目標 =====================
#
# 最終應該學習到：
#   w ≈ 2.0（因為 y = 2x + 1）
#   b ≈ 1.0
#
# 這證明了自動微分引擎可以正確地優化參數！